package com.example.colorcodelist;

public class ColorCodeItem {

    private int id;
    private int title;

    ColorCodeItem(int id, int title) {
        this.id = id;
        this.title = title;
    }

    int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    int getTitle() {
        return title;
    }

    public void setTitle(int title) {
        this.title = title;
    }




}
